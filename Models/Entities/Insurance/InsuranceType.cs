using System.ComponentModel;

namespace ClinicApp.Models.Entities.Insurance
{
    /// <summary>
    /// نوع بیمه - طراحی شده برای سیستم‌های پزشکی کلینیک شفا
    /// 
    /// ویژگی‌های کلیدی:
    /// 1. تمایز بین بیمه اصلی و تکمیلی
    /// 2. پشتیبانی از محاسبات متفاوت
    /// 3. مدیریت تنظیمات خاص
    /// 4. رعایت استانداردهای پزشکی ایران
    /// 
    /// نکته حیاتی: این enum برای تمایز بین انواع مختلف بیمه در سیستم استفاده می‌شود
    /// </summary>
    public enum InsuranceType
    {
        /// <summary>
        /// بیمه اصلی - بیمه پایه که توسط سازمان‌های بیمه‌گر اصلی ارائه می‌شود
        /// مثال: تأمین اجتماعی، خدمات درمانی، نیروهای مسلح
        /// </summary>
        [Description("بیمه اصلی")]
        Primary = 1,

        /// <summary>
        /// بیمه تکمیلی - بیمه‌های اضافی که برای تکمیل پوشش بیمه اصلی استفاده می‌شوند
        /// مثال: بیمه تکمیلی درمان، بیمه تکمیلی دندانپزشکی
        /// </summary>
        [Description("بیمه تکمیلی")]
        Supplementary = 2
    }
}
